public class Calculator {


    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int div(int x, int y) {
        return x / y;
    }

    public int mult(int x, int y){
        return x * y;
    }
}
